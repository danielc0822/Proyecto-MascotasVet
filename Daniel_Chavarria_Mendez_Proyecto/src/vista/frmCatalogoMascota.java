    private void TableDatosMascotasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableDatosMascotasMouseClicked

        try {
            int fila = TableDatosMascotas.getSelectedRow();
            txtID.setText(TableDatosMascotas.getValueAt(fila, 0).toString());
            txtNombre.setText(TableDatosMascotas.getValueAt(fila, 1).toString());
            cbGenero.setSelectedItem(TableDatosMascotas.getValueAt(fila, 2));

            txtTipo.setText(TableDatosMascotas.getValueAt(fila, 3).toString());
            txtRaza.setText(TableDatosMascotas.getValueAt(fila, 4).toString());

            String Activo = TableDatosMascotas.getValueAt(fila, 5).toString();

            if ("true".equals(Activo)) {

                rbActivo.setSelected(true);
            } else {
                rbActivo.setSelected(false);
            }
            txtIdDueno.setText(TableDatosMascotas.getValueAt(fila, 6).toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al seleccionar un registro: " + e.getMessage());
        }
    }//GEN-LAST:event_TableDatosMascotasMouseClicked

